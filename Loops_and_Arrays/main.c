float Our_Pow (float base, int exp) //выполнение функции возведения в степень числа
{
    float result = base; // ввожу вещественную переменную result и присваиваю ей значение входного параметра base (число, которое функция будет возводить в степень exp),
    
    for (int i=2; i<=exp; i++) // выполнение цикла со счетчиком: целочисленная переменная i увеличивается на единицу (i++ = i=i+1) до тех пор, пока не станет равной входному параметру степени exp; как только она превысит это значение, произойдет завершение работы цикла
        // i=2 потому, что перед выполнением цикла выражение float result = base возвело наше основание в степень 1
    {
        result = result * base;
        
    }
    printf("%f\n", result); // вывод полученного результата работы цикла в консоль
    return result; // возвращение результата работы функции
}

void Multiplication_Table_for_Number (int a) // выполнение ф-ции написания таблицы умножения для задаваемого числа
{
    for (int i=1; i<=10; i++) // выполнение цикла со счетчиком: целочисленная переменная i увеличивается на единицу до тех пор, пока не станет равной 10, после этого произойдет выход из цикла
    {
        printf("%d x %d = %d\n", a, i, a*i); // вывод чисел, которые умножаются и результатов умножения в консоль
    }
}

