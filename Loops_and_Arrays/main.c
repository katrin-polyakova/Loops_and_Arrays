void Array_Find_Zero() // выполнение функции вывода (и сохранения в отдельный массив) индексов всех элементов массива, которые равны нулю
{
    int arr [9] = {5,0,18,3,4,0,9,2,0}; // создаю массив, который состоит из 9 элементов
    int results [9] = {0}; // создаю пустой массив (значения элементов равны 0), который состоит тоже из 9 элементов; в него будут сохраняться индексы элементов массива int arr [9]
    int m=0; // инициализирую переменную m, она нужна для записи индекса элемента в пустой массив
    for (int i=0; i<9; i++) // в цикле со счетчиком перебираю элементы массива
    {
        if (arr[i]==0) // если значение элемента массива int arr [9] равно 0, то выполнить следующее:
        {
            results [m]=i; // в массив results [9] в ячейку m положить значение индекса элемента массива int arr [9]
            m++; // переход к следующей ячейке массива results [9]
            printf("index is %d\n", i); // вывод в консоль значение индекса элемента массива int arr [9]
        }
    }
}
